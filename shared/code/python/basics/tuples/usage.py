my_tuple = (1, 2, 3)  # Création d'un n-uplet
my_tuple[0]  # Premier élément : 1
my_tuple[1]  # Deuxième élément : 2
my_tuple[-1]  # Dernier élément : 3
my_tuple[-2]  # Avant-dernier élément : 2
1 in my_tuple  # True
4 in my_tuple  # False
my_tuple + (4, 5, 6)  # (1, 2, 3, 4, 5, 6)
len(my_tuple)  # Taille du n-uplet : 3
