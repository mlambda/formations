my_list = []  # Création d'une liste vide
my_list = [1, 2, 3]  # Création d'une liste avec des éléments initiaux
my_list[0]  # Premier élément : 1
my_list[1]  # Deuxième élément : 2
my_list[-1]  # Dernier élément : 3
my_list[-2]  # Avant-dernier élément : 2
my_list.append(4)  # Ajout de l'élément 4
my_list.extend([5, 6])  # Ajout des éléments 5 et 6
my_list.sort(reverse=True)  # Tri inversé de la liste
len(my_list)  # Taille de la liste : 6
del my_list[4]  # Supprime le 5e élément
